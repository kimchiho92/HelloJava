package hijava.practice;

public class Variable {
	public static void main(String[]args) {
		int a= 12;
		float b=  2.4f;
		int r1=(int)(a+b);
		int r2=(int)(a*b);
		System.out.println(r1);
		System.out.println(r2);
		System.out.println("++++++++++++++++++++");
		System.out.println(a+b);
		System.out.println(a*b);
		
		
		double d= 12.4;
		int i= 3;
		System.out.println("================================");
		System.out.println(d%i);
		System.out.println((int)d%i);
		
	}
}
